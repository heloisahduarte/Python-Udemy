"""
Python = Linguagem de Programaçao
Tipo de Tipagem = Dinâmica/Forte

str -> string -> texto
Strings são textos dentro de aspas
"""

# Aspas simples 
print ('Heloisa Duarte')
# Aspas duplas
print ("Heloisa  Duarte")
# Escape
print ("Heloisa Duarte \" Programadora \"")
# Usada para exibir as "" dentro de uma string (Executar o código para melhor entendimento)

# r
print (r"Heloisa Duarte \" Programadora \"")
#Usado para exibir o \"\" dentro de uma string (Executar o código para melhor entendimento)

"""
Para evitar o uso do Escape e do r, basta aspas duplas dentro de aspas simples e vice-versa
"""
print ('"Heloisa Duarte"')
